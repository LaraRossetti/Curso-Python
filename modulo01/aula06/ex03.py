n = input('Digite algo: ')
print(n.isalpha())    #verifica se é alfabético
print(n.isalnum())    #verifica se é alfabético ou numérico
print(n.isnumeric())  #verifica se é numérico
print(n.isspace())    #verifica se é espaço
print(n.islower())    #verifica se todos os caracteres estão em minúsculo......

